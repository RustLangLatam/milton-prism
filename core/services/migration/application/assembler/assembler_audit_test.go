// TestFrente2a_TidyGoMod proves the platform/agent-only direct + indirect requires
// are pruned from the assembled go.mod, while the deliverable's real deps survive.
func TestFrente2a_TidyGoMod(t *testing.T) {
	root := buildProtoClosureFixture(t)
	a := New(root, false, "go", "grpc", "")
	files, err := a.Assemble([]InputFile{{Path: "core/cmd/user-services/main.go", Content: "package main\n"}})
	if err != nil {
		t.Fatalf("Assemble: %v", err)
	}
	var gomod string
	for _, f := range files {
		if f.Path == "go.mod" {
			gomod = string(f.Content)
		}
	}
	if gomod == "" {
		t.Fatal("go.mod missing")
	}
	for _, dropped := range []string{
		"github.com/docker/docker",
		"github.com/go-git/go-git/v5",
		"github.com/go-enry/go-enry/v2",
		"github.com/smacker/go-tree-sitter",
		"github.com/hibiken/asynq",
		"github.com/ProtonMail/go-crypto",
		"github.com/redis/go-redis/v9",
		"github.com/moby/term",
	} {
		if strings.Contains(gomod, dropped) {
			t.Errorf("go.mod still declares pruned platform dep %q:\n%s", dropped, gomod)
		}
	}
	// Deliverable's real deps survive.
	for _, kept := range []string{"github.com/google/uuid", "github.com/stretchr/objx"} {
		if !strings.Contains(gomod, kept) {
			t.Errorf("go.mod wrongly dropped real dep %q:\n%s", kept, gomod)
		}
	}
}

// TestFrente2a_TidyGoModUnit drives tidyGoMod directly over a representative go.mod.
func TestFrente2a_TidyGoModUnit(t *testing.T) {
	merged := map[string][]byte{
		"go.mod": []byte("module milton_prism\n\ngo 1.25.0\n\nrequire (\n\tgithub.com/docker/docker v27.1.1+incompatible\n\tgithub.com/google/uuid v1.6.0\n)\n\nrequire (\n\tgithub.com/go-git/go-billy/v5 v5.9.0 // indirect\n\tgithub.com/spf13/cast v1.10.0 // indirect\n)\n"),
	}
	tidyGoMod(merged)
	out := string(merged["go.mod"])
	if strings.Contains(out, "github.com/docker/docker") {
		t.Errorf("docker not pruned:\n%s", out)
	}
	if strings.Contains(out, "go-git/go-billy") {
		t.Errorf("go-git indirect not pruned:\n%s", out)
	}
	if !strings.Contains(out, "github.com/google/uuid") || !strings.Contains(out, "github.com/spf13/cast") {
		t.Errorf("non-platform deps wrongly pruned:\n%s", out)
	}
	if !strings.Contains(out, "module milton_prism") || !strings.Contains(out, "go 1.25.0") {
		t.Errorf("module/go directives clobbered:\n%s", out)
	}
}

// TestFrente2a_PythonConfigPrune proves the Python pyproject mypy overrides + the
// .importlinter are rewritten to the generated service (no platform identity/
// repository/migration leakage), and fastapi/uvicorn are dropped for a gRPC cell.
func TestFrente2a_PythonConfigPrune(t *testing.T) {
	root := buildProtoClosureFixture(t)
	// Provide a realistic platform pyproject + importlinter in the skeleton.
	writeFixture(t, root, "python/__init__.py", "")
	writeFixture(t, root, "python/pyproject.toml", `[tool.poetry.dependencies]
python = "^3.12"
fastapi = "^0.115.0"
uvicorn = "^0.34.0"
grpcio = "^1.68.0"
pydantic = "^2.10.0"

[[tool.mypy.overrides]]
module = [
    "services.identity.infrastructure.grpc_handlers.identity_handler",
    "services.identity.__main__",
    "shared.auth.extractor",
]
ignore_errors = true
`)
	writeFixture(t, root, "python/.importlinter", `[importlinter]
root_packages =
    shared
    services
include_external_packages = True

[importlinter:contract:identity-domain-independence]
name = Identity domain may not import ports, application, or infrastructure
type = forbidden
source_modules =
    services.identity.domain
forbidden_modules =
    services.identity.infrastructure
    grpc
`)
	a := New(root, false, "python", "grpc", "")
	artifacts := []InputFile{
		{Path: "python/services/user/__main__.py", Content: "x = 1\n"},
		{Path: "python/services/user/domain/user.py", Content: "class User: ...\n"},
	}
	files, err := a.Assemble(artifacts)
	if err != nil {
		t.Fatalf("Assemble: %v", err)
	}
	var pyproject, linter string
	for _, f := range files {
		switch f.Path {
		case "core/pyproject.toml":
			pyproject = string(f.Content)
		case "core/.importlinter":
			linter = string(f.Content)
		}
	}
	if pyproject == "" || linter == "" {
		t.Fatalf("pyproject/.importlinter missing (pyproject present=%v linter present=%v)", pyproject != "", linter != "")
	}
	// mypy overrides: platform service gone, generated service present, non-service kept.
	if strings.Contains(pyproject, "services.identity.") {
		t.Errorf("pyproject still references platform service services.identity.*:\n%s", pyproject)
	}
	if !strings.Contains(pyproject, "services.user.") {
		t.Errorf("pyproject did not remap mypy override to generated service services.user.*:\n%s", pyproject)
	}
	if !strings.Contains(pyproject, "shared.auth.extractor") {
		t.Errorf("pyproject dropped the non-service override shared.auth.extractor:\n%s", pyproject)
	}
	// fastapi/uvicorn dropped for gRPC.
	for _, dep := range []string{"fastapi", "uvicorn"} {
		if strings.Contains(pyproject, dep+" = \"") {
			t.Errorf("gRPC Python deliverable kept unused dep %q:\n%s", dep, pyproject)
		}
	}
	// .importlinter rewritten to the generated service.
	if strings.Contains(linter, "services.identity.") {
		t.Errorf(".importlinter still references platform service identity:\n%s", linter)
	}
	if !strings.Contains(linter, "services.user.domain") {
		t.Errorf(".importlinter not regenerated for generated service user:\n%s", linter)
	}
}

// TestFrente3_StoreSpecificEnvDefaults proves the SQL .env.example DB defaults match
// the cell's engine: MySQL/MariaDB → port 3306 + mysql URL; Postgres → 5432.
func TestFrente3_StoreSpecificEnvDefaults(t *testing.T) {
	cases := []struct {
		profile, store, root, wantPort, wantScheme, badPort string
	}{
		{"rust", "mysql", "rust", "DB_PORT=3306", "mysql://", "DB_PORT=5432"},
		{"rust", "postgres", "rust", "DB_PORT=5432", "postgres://", "DB_PORT=3306"},
		{"go", "mysql", "core", "DB_PORT=3306", "tcp(", "DB_PORT=5432"},
		{"python", "mysql", "python", "DB_PORT=3306", "aiomysql", "DB_PORT=5432"},
		{"node", "mysql", "node", "DB_PORT=3306", "mysql://", "DB_PORT=5432"},
	}
	for _, tc := range cases {
		t.Run(tc.profile+"_"+tc.store, func(t *testing.T) {
			root := buildSkeletonFixture(t)
			a := New(root, false, tc.profile, "grpc", tc.store)
			var artPath string
			switch tc.profile {
			case "go":
				artPath = "core/cmd/user-services/main.go"
			default:
				artPath = tc.root + "/services/user/main.txt"
			}
			files, err := a.Assemble([]InputFile{{Path: artPath, Content: "x\n"}})
			if err != nil {
				t.Fatalf("Assemble: %v", err)
			}
			var env string
			for _, f := range files {
				if strings.HasSuffix(f.Path, "user/.env.example") || strings.HasSuffix(f.Path, "user-services/.env.example") {
					env = string(f.Content)
				}
			}
			if env == "" {
				t.Fatalf("no .env.example produced")
			}
			if !strings.Contains(env, tc.wantPort) {
				t.Errorf("%s+%s .env.example missing %q; got:\n%s", tc.profile, tc.store, tc.wantPort, env)
			}
			if strings.Contains(env, tc.badPort) {
				t.Errorf("%s+%s .env.example has wrong-engine %q; got:\n%s", tc.profile, tc.store, tc.badPort, env)
			}
			if !strings.Contains(env, tc.wantScheme) {
				t.Errorf("%s+%s .env.example missing %q DSN scheme; got:\n%s", tc.profile, tc.store, tc.wantScheme, env)
			}
		})
	}
}

// TestAssemble_JavaGRPC_RelocatesVendoredProtos proves the Java guardrail: the
// agent's vendored well-known / google.api protos (written under
// java/services/<svc>/src/main/proto-include/google/…) are relocated to the
// canonical protobuf/proto/google/… tree, NO .proto survives under core/, and the
// per-service pom.xml is rewritten to drop the proto-include
// <additionalProtoPathElement> and repoint <protoSourceRoot> at the canonical
// ../../../protobuf/proto path. It is the Java homologue of
// TestAssemble_RustGRPC_RelocatesVendoredProtos.
func TestAssemble_JavaGRPC_RelocatesVendoredProtos(t *testing.T) {
	root := buildSkeletonFixture(t)

	a := New(root, true, "java", "grpc", "")

	pom := `<project>
  <artifactId>user</artifactId>
  <build>
    <plugins>
      <plugin>
        <artifactId>protobuf-maven-plugin</artifactId>
        <configuration>
          <protoSourceRoot>${project.basedir}/src/main/proto</protoSourceRoot>
          <additionalProtoPathElement>${project.basedir}/src/main/proto-include</additionalProtoPathElement>
        </configuration>
      </plugin>
    </plugins>
  </build>
</project>
`
	artifacts := []InputFile{
		{Path: "java/services/user/pom.xml", Content: pom},
		{Path: "java/services/user/src/main/java/com/example/user/UserApplication.java", Content: "package com.example.user;\n"},
		// Vendored third-party protos under the proto-include dir.
		{Path: "java/services/user/src/main/proto-include/google/api/annotations.proto", Content: "syntax = \"proto3\";\npackage google.api;\n"},
		{Path: "java/services/user/src/main/proto-include/google/api/http.proto", Content: "syntax = \"proto3\";\npackage google.api;\n"},
		{Path: "java/services/user/src/main/proto-include/google/api/field_behavior.proto", Content: "syntax = \"proto3\";\npackage google.api;\n"},
		// A second service vendoring the SAME proto — must dedup, not collide.
		{Path: "java/services/order/src/main/proto-include/google/api/annotations.proto", Content: "syntax = \"proto3\";\npackage google.api;\n"},
		{Path: "java/services/order/pom.xml", Content: pom},
	}
	files, err := a.Assemble(artifacts)
	if err != nil {
		t.Fatalf("Assemble: %v", err)
	}
	set := pathSet(files)

	// Invariant: zero .proto under core/ and no proto-include dir survives.
	for _, f := range files {
		if strings.HasPrefix(f.Path, "core/") && strings.HasSuffix(f.Path, ".proto") {
			t.Errorf("proto leaked under core/: %q", f.Path)
		}
		if strings.Contains(f.Path, "/proto-include/") {
			t.Errorf("proto-include dir survived: %q", f.Path)
		}
	}

	// Relocated to the canonical protobuf/proto/ tree (untouched by java/→core/).
	for _, want := range []string{
		"protobuf/proto/google/api/annotations.proto",
		"protobuf/proto/google/api/http.proto",
		"protobuf/proto/google/api/field_behavior.proto",
	} {
		if !set[want] {
			t.Errorf("vendored proto not relocated to canonical path: %q missing", want)
		}
	}

	// pom.xml rewritten: no proto-include additionalProtoPathElement; protoSourceRoot
	// repointed at the canonical tree.
	var gotPom string
	for _, f := range files {
		if f.Path == "core/services/user/pom.xml" {
			gotPom = string(f.Content)
		}
	}
	if gotPom == "" {
		t.Fatal("core/services/user/pom.xml missing")
	}
	if strings.Contains(gotPom, "proto-include") {
		t.Errorf("pom.xml still references proto-include:\n%s", gotPom)
	}
	if strings.Contains(gotPom, "additionalProtoPathElement") {
		t.Errorf("pom.xml still carries an additionalProtoPathElement:\n%s", gotPom)
	}
	if !strings.Contains(gotPom, "<protoSourceRoot>../../../protobuf/proto</protoSourceRoot>") {
		t.Errorf("pom.xml protoSourceRoot not repointed at canonical ../../../protobuf/proto:\n%s", gotPom)
	}
}

// TestAudit_NoProtoUnderCore_AllProfiles is the lock-in for TASK A: after assembly,
// NO `.proto` file may live anywhere under core/ for ANY of the five language
// profiles. Each profile is fed a generated service proto under the canonical
// protobuf/proto tree PLUS a stray/vendored .proto under its source tree (which the
// rename would otherwise carry to core/); the assembler must keep every .proto under
// protobuf/proto/ only.
func TestAudit_NoProtoUnderCore_AllProfiles(t *testing.T) {
	cases := []struct {
		profile string
		// vendored is a .proto the agent wrote under the profile's source tree (the
		// thing that, left in place, would land under core/ after the rename).
		vendored InputFile
		// extra source artifact so the profile produces a real deliverable.
		src InputFile
	}{
		{
			profile:  "go",
			vendored: InputFile{Path: "core/services/user/proto/user.proto", Content: "syntax=\"proto3\";\n"},
			src:      InputFile{Path: "core/cmd/user-services/main.go", Content: "package main\n"},
		},
		{
			profile:  "python",
			vendored: InputFile{Path: "python/services/user/proto/user.proto", Content: "syntax=\"proto3\";\n"},
			src:      InputFile{Path: "python/services/user/__main__.py", Content: "x = 1\n"},
		},
		{
			profile:  "node",
			vendored: InputFile{Path: "node/services/user/proto/user.proto", Content: "syntax=\"proto3\";\n"},
			src:      InputFile{Path: "node/services/user/src/main.ts", Content: "export {}\n"},
		},
		{
			profile:  "rust",
			vendored: InputFile{Path: "rust/services/user/proto_include/google/api/annotations.proto", Content: "syntax=\"proto3\";\n"},
			src:      InputFile{Path: "rust/services/user/src/main.rs", Content: "fn main() {}\n"},
		},
		{
			profile:  "java",
			vendored: InputFile{Path: "java/services/user/src/main/proto-include/google/api/annotations.proto", Content: "syntax=\"proto3\";\n"},
			src:      InputFile{Path: "java/services/user/src/main/java/com/example/user/UserApplication.java", Content: "package com.example.user;\n"},
		},
	}
	for _, tc := range cases {
		t.Run(tc.profile, func(t *testing.T) {
			root := buildSkeletonFixture(t)
			a := New(root, true, tc.profile, "grpc", "")
			files, err := a.Assemble([]InputFile{
				tc.src,
				tc.vendored,
				// A legitimately-placed generated service proto under the canonical tree.
				{Path: "protobuf/proto/user/v1/user.proto", Content: "syntax=\"proto3\";\n"},
			})
			if err != nil {
				t.Fatalf("Assemble: %v", err)
			}
			for _, f := range files {
				if strings.HasPrefix(f.Path, "core/") && strings.HasSuffix(f.Path, ".proto") {
					t.Errorf("[%s] .proto leaked under core/: %q", tc.profile, f.Path)
				}
			}
			// Sanity: the canonical proto still ships (assembler kept the proto tree).
			if !pathSet(files)["protobuf/proto/user/v1/user.proto"] {
				t.Errorf("[%s] canonical service proto dropped from protobuf/proto/", tc.profile)
			}
		})
	}
}

