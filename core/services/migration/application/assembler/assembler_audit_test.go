// TestFrente1_GoProtoClosureAndBuf proves that a Go deliverable carrying the
// generated service proto pulls the FULL transitive import closure (pagination,
// query_params, openapiv3/annotations, openapiv3/OpenAPIv3) out of the skeleton,
// ships buf.lock, drops the over-vendored go_features.proto, and strips the stale
// buf.yaml ignore for proto/openapi-spec.proto while keeping the live proto/openapiv3
// ignore (openapiv3 now ships).
func TestFrente1_GoProtoClosureAndBuf(t *testing.T) {
	root := buildProtoClosureFixture(t)
	a := New(root, false, "go", "grpc", "")
	artifacts := []InputFile{
		{Path: "core/cmd/user-services/main.go", Content: "package main\n"},
		{Path: "protobuf/proto/milton_prism/services/user/v1/user_service.proto", Content: userServiceProtoArtifact},
		// An over-vendored proto the agent shipped that no service imports.
		{Path: "protobuf/proto/google/protobuf/go_features.proto", Content: "syntax=\"proto3\";\n"},
	}
	files, err := a.Assemble(artifacts)
	if err != nil {
		t.Fatalf("Assemble: %v", err)
	}
	set := pathSet(files)

	// FRENTE 1: full closure present.
	for _, want := range []string{
		"protobuf/proto/milton_prism/services/user/v1/user_service.proto",
		"protobuf/proto/milton_prism/types/pagination/v1/pagination.proto",
		"protobuf/proto/milton_prism/types/query_params/v1/query_params.proto",
		"protobuf/proto/openapiv3/annotations.proto",
		"protobuf/proto/openapiv3/OpenAPIv3.proto",
		"protobuf/buf.lock",
	} {
		if !set[want] {
			t.Errorf("Go deliverable missing closure/buf file %q", want)
		}
	}
	// FRENTE 1: over-vendored proto pruned.
	if set["protobuf/proto/google/protobuf/go_features.proto"] {
		t.Errorf("Go deliverable shipped over-vendored go_features.proto (no service imports it)")
	}
	// FRENTE 1: stale buf.yaml ignore for the never-shipped openapi-spec.proto stripped;
	// the live openapiv3 ignore kept (openapiv3 ships).
	var buf string
	for _, f := range files {
		if f.Path == "protobuf/buf.yaml" {
			buf = string(f.Content)
		}
	}
	if strings.Contains(buf, "openapi-spec.proto") {
		t.Errorf("buf.yaml kept stale dangling ignore proto/openapi-spec.proto:\n%s", buf)
	}
	if !strings.Contains(buf, "proto/openapiv3") {
		t.Errorf("buf.yaml dropped the live proto/openapiv3 ignore (openapiv3 ships):\n%s", buf)
	}
	// FRENTE 2a: phpclassify never ships.
	if set["core/shared/phpclassify/segment.go"] {
		t.Errorf("Go deliverable leaked platform phpclassify code")
	}
}

